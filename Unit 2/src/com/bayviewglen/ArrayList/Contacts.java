package com.bayviewglen.ArrayList;

public class Contacts {
	private String lname;
	private String fname;
	private String phone;

	public Contacts(){
		lname = null ;
		fname = null;
		phone = null;	
	}
	
	public String getLastname(){
		return lname;
	}
	
	public String getFirstName(){
		return fname;
	}
	
	public String getPhoneNumber(){
		return phone;
	}

	
	
	
}
